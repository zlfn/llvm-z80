#ifndef LLVM_Z80_INST_PRINTER_H
#define LLVM_Z80_INST_PRINTER_H

#include "MCTargetDesc/Z80MCTargetDesc.h"
#include "llvm/MC/MCInstPrinter.h"
#include <utility>

namespace llvm {

/// Prints Z80 instructions to a textual stream.
class Z80InstPrinter : public MCInstPrinter {
public:
  Z80InstPrinter(const MCAsmInfo &MAI, const MCInstrInfo &MII,
                 const MCRegisterInfo &MRI)
      : MCInstPrinter(MAI, MII, MRI) {}

  bool printAliasInstr(const MCInst *MI, uint64_t Address, raw_ostream &OS);

  void printInst(const MCInst *MI, uint64_t Address, StringRef Annot,
                 const MCSubtargetInfo &STI, raw_ostream &OS) override;
  void printRegName(raw_ostream &OS, MCRegister Reg) override;

  std::pair<const char *, uint64_t>
  getMnemonic(const MCInst &MI) const override;

  // generated by TableGen
  void printInstruction(const MCInst *MI, uint64_t Address, raw_ostream &OS);
  virtual void printOperand(const MCInst *MI, unsigned OpNo, raw_ostream &OS);
  void printBranchOperand(const MCInst *MI, uint64_t Address, unsigned OpNo,
                          raw_ostream &OS);
  void printIndexedOperand(const MCInst *MI, unsigned OpNo, raw_ostream &OS);
  void printCustomAliasOperand(const MCInst *MI, uint64_t Address,
                               unsigned OpIdx, unsigned PrintMethodIdx,
                               raw_ostream &OS);
  static const char *getRegisterName(MCRegister Reg);
  static const char *getRegisterName(MCRegister Reg, unsigned AltIdx);

  /// Utility function to print immediates in decimal or hex.
  format_object<int64_t> formatImm(int64_t Value) const {
    return PrintImmHex ? formatHex(Value) : formatDec(Value);
  }

  /// Utility functions to print decimal/hexadecimal values.
  format_object<int64_t> formatHex(int64_t Value) const;
  format_object<uint64_t> formatHex(uint64_t Value) const;
};

/// Prints Z80 instructions in sdasz80 (SDCC assembler) compatible syntax.
/// Key differences from standard Zilog:
///   - Immediates prefixed with # (e.g., ld a,#5)
///   - Indexed addressing: offset(ix) instead of (ix+offset)
///   - Hex format: 0xFF instead of $FF
class Z80InstPrinterSDCC : public Z80InstPrinter {
public:
  Z80InstPrinterSDCC(const MCAsmInfo &MAI, const MCInstrInfo &MII,
                     const MCRegisterInfo &MRI)
      : Z80InstPrinter(MAI, MII, MRI) {}

  void printInst(const MCInst *MI, uint64_t Address, StringRef Annot,
                 const MCSubtargetInfo &STI, raw_ostream &OS) override;
  void printOperand(const MCInst *MI, unsigned OpNo, raw_ostream &OS) override;

  format_object<int64_t> formatHex(int64_t Value) const;
  format_object<uint64_t> formatHex(uint64_t Value) const;

  format_object<int64_t> formatImm(int64_t Value) const {
    return PrintImmHex ? formatHex(Value) : formatDec(Value);
  }
};

} // end namespace llvm

#endif // LLVM_Z80_INST_PRINTER_H
