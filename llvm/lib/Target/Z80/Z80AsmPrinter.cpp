class Z80AsmPrinter : public AsmPrinter {
  Z80MCInstLower InstLowering;

public:
  explicit Z80AsmPrinter(TargetMachine &TM,
                         std::unique_ptr<MCStreamer> Streamer)
      : AsmPrinter(TM, std::move(Streamer)), InstLowering(OutContext, *this) {}

  // Generated by TableGen.
  bool lowerPseudoInstExpansion(const MachineInstr *MI, MCInst &Inst);

  // Wrapper needed for tblgenned pseudo lowering.
  void lowerOperand(const MachineOperand &MO, MCOperand &MCOp);

  const MCExpr *lowerConstant(const Constant *CV,
                              const Constant *BaseCV = nullptr,
                              uint64_t Offset = 0) override;

  void EmitToStreamer(MCStreamer &S, MCInst &Inst);

  void emitInstruction(const MachineInstr *MI) override;

  bool PrintAsmOperand(const MachineInstr *MI, unsigned OpNo,
                       const char *ExtraCode, raw_ostream &OS) override;

  bool PrintAsmMemoryOperand(const MachineInstr *MI, unsigned OpNo,
                             const char *ExtraCode, raw_ostream &OS) override;

  void emitStartOfAsmFile(Module &M) override;

  void emitGlobalVariable(const GlobalVariable *GV) override;

  void emitJumpTableInfo() override;
};

// Simple pseudo-instructions have their lowering (with expansion to real
// instructions) auto-generated.
#include "Z80GenMCPseudoLowering.inc"

const MCExpr *Z80AsmPrinter::lowerConstant(const Constant *CV,
                                           const Constant *BaseCV,
                                           uint64_t Offset) {
  return AsmPrinter::lowerConstant(CV);
}

void Z80AsmPrinter::EmitToStreamer(MCStreamer &S, MCInst &Inst) {
  Z80AsmBackend::translateOpcodeToSubtarget(Inst, MF->getSubtarget());
  // If this instruction contains an out-of-range immediate address, perform an
  // early relax.
  Z80AsmBackend::relaxForImmediate(Inst, MF->getSubtarget<Z80Subtarget>());
  AsmPrinter::EmitToStreamer(S, Inst);
}

void Z80AsmPrinter::emitInstruction(const MachineInstr *MI) {
  // Do any auto-generated pseudo lowerings.
  if (MCInst OutInst; lowerPseudoInstExpansion(MI, OutInst)) {
    EmitToStreamer(*OutStreamer, OutInst);
    return;
  }

  MCInst Inst;
  InstLowering.lower(MI, Inst);
  EmitToStreamer(*OutStreamer, Inst);
}

void Z80AsmPrinter::lowerOperand(const MachineOperand &MO, MCOperand &MCOp) {
  if (!InstLowering.lowerOperand(MO, MCOp))
    llvm_unreachable("Failed to lower operand.");
}

bool Z80AsmPrinter::PrintAsmOperand(const MachineInstr *MI, unsigned OpNo,
                                    const char *ExtraCode, raw_ostream &OS) {
  if (!AsmPrinter::PrintAsmOperand(MI, OpNo, ExtraCode, OS))
    return false;

  const MachineOperand &MO = MI->getOperand(OpNo);
  const MachineFunction &MF = *MO.getParent()->getMF();
  const Z80RegisterInfo &TRI =
      *MF.getSubtarget<Z80Subtarget>().getRegisterInfo();

  switch (MO.getType()) {
  default:
    errs() << "Unsupported inline asm operand: " << MO << "\n";
    return true;
  case MachineOperand::MO_Immediate:
    OS << MO.getImm();
    break;
  case MachineOperand::MO_GlobalAddress:
    OS << MO.getGlobal()->getName();
    break;
  case MachineOperand::MO_Register: {
    Register Reg = MO.getReg();
    OS << TRI.getRegAsmName(Reg);
    break;
  }
  }
  return false;
}

bool Z80AsmPrinter::PrintAsmMemoryOperand(const MachineInstr *MI, unsigned OpNo,
                                          const char *ExtraCode,
                                          raw_ostream &OS) {
  // Memory operands are simply stored in pointer registers; no extra work is
  // required.
  return PrintAsmOperand(MI, OpNo, ExtraCode, OS);
}

