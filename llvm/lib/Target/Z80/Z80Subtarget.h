#include "Z80LegalizerInfo.h"
#include "Z80RegisterBankInfo.h"
#include "Z80RegisterInfo.h"
#include "llvm/CodeGen/GlobalISel/CallLowering.h"

#define GET_SUBTARGETINFO_HEADER
#include "Z80GenSubtargetInfo.inc"

namespace llvm {

class Z80TargetMachine;

/// A specific Z80 target MCU.
class Z80Subtarget : public Z80GenSubtargetInfo {
public:
  Z80Subtarget(const Triple &TT, const std::string &CPU, const std::string &FS,
               const Z80TargetMachine &TM);

  const Z80FrameLowering *getFrameLowering() const override {
    return &FrameLowering;
  }

  const Z80InstrInfo *getInstrInfo() const override { return &InstrInfo; }

  const Z80RegisterInfo *getRegisterInfo() const override { return &RegInfo; }

  const Z80TargetLowering *getTargetLowering() const override {
    return &TLInfo;
  }

  const CallLowering *getCallLowering() const override {
    return CallLoweringInfo.get();
  }

  const LegalizerInfo *getLegalizerInfo() const override { return &Legalizer; }

  const RegisterBankInfo *getRegBankInfo() const override {
    return &RegBankInfo;
  }

  InstructionSelector *getInstructionSelector() const override {
    return InstSelector.get();
  }

  const Z80InlineAsmLowering *getInlineAsmLowering() const override {
    return &InlineAsmLoweringInfo;
  }

  // Z80/SM83 are simple in-order processors with no pipeline.  The pre-RA
  // machine scheduler's reschedulePhysReg() delays COPY instructions to
  // shorten virtual register live ranges, but extends physical register
  // live ranges — causing register exhaustion on register-constrained code.
  bool enableMachineScheduler() const override { return false; }
  bool enableSubRegLiveness() const override { return true; }

  bool useAA() const override { return true; }

  bool staticStack() const { return StaticStack; }

  // Feature queries
  bool hasZ80() const { return HasZ80; }
  bool hasSM83() const { return HasSM83; }
  bool hasUndocumented() const { return HasUndocumented; }

  Z80Subtarget &initializeSubtargetDependencies(StringRef CPU, StringRef FS,
                                                const TargetMachine &TM);

  /// Parses a subtarget feature string, setting appropriate options.
  /// \note Definition of function is auto generated by `tblgen`.
  void ParseSubtargetFeatures(StringRef CPU, StringRef TuneCPU, StringRef FS);

private:
  // Subtarget feature settings
  // Z80-specific features
  bool HasZ80 = false;
  bool HasZ180 = false;
  bool HasEZ80 = false;
  bool HasR800 = false;
  bool HasUndocumented = false;

  // SM83 (Game Boy CPU)
  bool HasSM83 = false;

  bool StaticStack = false;

  Z80InstrInfo InstrInfo;
  Z80RegisterInfo RegInfo;
  Z80FrameLowering FrameLowering;
  Z80TargetLowering TLInfo;
  std::unique_ptr<CallLowering> CallLoweringInfo;
  Z80LegalizerInfo Legalizer;
  Z80RegisterBankInfo RegBankInfo;
  std::unique_ptr<InstructionSelector> InstSelector;
  Z80InlineAsmLowering InlineAsmLoweringInfo;
};

} // end namespace llvm

#endif // LLVM_Z80_SUBTARGET_H
